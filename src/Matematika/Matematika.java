package Matematika;



public class Matematika {
    void pertambahan(int a, int b) {
        int hasil = a + b;
        System.out.println("\n2 + 1");
        System.out.println("Hasil: " +hasil);
    }

    void perkalian(int a, int b) {
        int hasil = a * b;
        System.out.println("\n4 * 2");
        System.out.println("Hasil: " +hasil);
    }
}